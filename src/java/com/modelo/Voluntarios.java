
package com.modelo;

public class Voluntarios {
   private int id_vo;
   private String nombre_v;
   private int edad_v;
   private int tefo_v;
   private String correo_v;
   private String direccion;
   private String motivo;

    public Voluntarios() {
    }

    public int getId_vo() {
        return id_vo;
    }

    public void setId_vo(int id_vo) {
        this.id_vo = id_vo;
    }

    public String getNombre_v() {
        return nombre_v;
    }

    public void setNombre_v(String nombre_v) {
        this.nombre_v = nombre_v;
    }

    public int getEdad_v() {
        return edad_v;
    }

    public void setEdad_v(int edad_v) {
        this.edad_v = edad_v;
    }

    public int getTefo_v() {
        return tefo_v;
    }

    public void setTefo_v(int tefo_v) {
        this.tefo_v = tefo_v;
    }

    public String getCorreo_v() {
        return correo_v;
    }

    public void setCorreo_v(String correo_v) {
        this.correo_v = correo_v;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getMotivo() {
        return motivo;
    }

    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }
   
   
   
}
